CREATE TABLE PLAYER(
	ID UUID NOT NULL,
	NAME VARCHAR(255),
);

CREATE TABLE SESSION(
	ID UUID NOT NULL,
	DESCRIPTION VARCHAR(255),
	CREATED_DATE BIGINT
);

CREATE TABLE ROLL(
	ID UUID NOT NULL,
	DIE_FACE INT,
	QUANTITY INT,
	BONUS_INCREMENT INT,
	PLAYER_ID BIGINT,
	SESSION_ID BIGINT,
	SUM INT,
	ROLL_DATE BIGINT
);